using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace BasicAll_WhileLoop
{
    class Program
    {
        static void Main(string[] args)
        {
            // While Loop Counter
            var num = 100;

            while(num < 200) {
                num++;

                Console.WriteLine(num);
            }
        }
    }
}
